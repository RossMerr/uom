package uom

/*
Units of Measure

Inspired by the 'The Unified Code for Units of Measure' http://unitsofmeasure.org/ucum.html

Can safely represent number as large as a quadrillion (1 000 000 000 000 000 000 000 000)
down to a quadrillionth (0.000 000 000 000 000 000 000 001) using the big.Float.

All base units are stores in the SI (International System of Units) with support for the following.

### Units of measurement:-

* Mass
* Volume
* Length
* Temperature

Non SI support is provided for-

* U.S. survey lengths
* British Imperial lengths
* U.S. volumes
* British Imperial volumes
* Avoirdupois weights
* Fahrenheit
*/